     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $kode = $request->kode;
        $nama = $request->nama;
        $path =$request->path;
        for ($i=0; $i < count($path); $i++) { 
            $data = [
                'kode'=>$kode,
                'nama'=>$nama,
                'path'=>$path[$i]
            ];
            DB::table('cetakan')->insert($data);
        }
        return redirect()->route('mba.index')->with('success','Pengajuan cetakan berhasil dibuat');
    }

